    int nextRnd() {
		    if (previous == 0) {
                    return previous = random.nextInt(range) + 1;
                    }
                    else
                    {
		    int rnd = random.nextInt(range-1) + 1;
		    previous = (rnd < previous? rnd : rnd + 1);
		    return previous;
                    }
		  }
